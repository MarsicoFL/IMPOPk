def main():
    parser = argparse.ArgumentParser(description='Full chr2 IBD analysis')
    parser.add_argument('--max-pairs', type=int, default=25)
    parser.add_argument('--min-ibd-windows', type=int, default=100)
    parser.add_argument('--populations', nargs='+', default=['EUR', 'AFR', 'EAS'])
    parser.add_argument('--output-dir', type=Path,
                       default=Path(__file__).parent.parent / 'results')
    parser.add_argument('--example-tracks', type=int, default=3)

    args = parser.parse_args()

    args.output_dir.mkdir(parents=True, exist_ok=True)
    figures_dir = args.output_dir / 'figures'
    figures_dir.mkdir(exist_ok=True)
    json_dir = args.output_dir / 'json'
    json_dir.mkdir(exist_ok=True)

    print("=" * 70)
    print("FULL CHROMOSOME 2 IBD ANALYSIS")
    print("=" * 70)
    print(f"Populations: {args.populations}")
    print(f"Max pairs: {args.max_pairs}")
    print(f"Min IBD windows: {args.min_ibd_windows}")

    results_by_pop = {}

    for pop in args.populations:
        results = analyze_population(
            pop,
            max_pairs=args.max_pairs,
            min_ibd_windows=args.min_ibd_windows,
        )

        if results:
            results_by_pop[pop] = results

            # Save JSON
            json_data = {
                'population': pop,
                'chromosome': 'chr2',
                'n_pairs': len(results),
                'results': [r.to_dict() for r in results],
            }
            with open(json_dir / f'{pop}_chr2_full_results.json', 'w') as f:
                json.dump(json_data, f, indent=2)

            # Generate track plots
            sorted_results = sorted(results, key=lambda r: r.total_ibd_bp, reverse=True)
            for i, result in enumerate(sorted_results[:args.example_tracks]):
                output_path = figures_dir / f'{pop}_chr2_track_{i+1}_{result.sample_a}_{result.sample_b}.png'
                try:
                    import matplotlib.pyplot as plt
                    fig = plot_ibd_track(result, output_path)
                    plt.close(fig)
                except Exception as e:
                    print(f"  Warning: {e}")

    # Population comparison
    if len(results_by_pop) > 1:
        print("\nGenerating population comparison...")
        try:
            import matplotlib.pyplot as plt
            fig = plot_population_comparison(
                results_by_pop,
                figures_dir / 'chr2_full_population_comparison.png'
            )
            plt.close(fig)
        except Exception as e:
            print(f"  Warning: {e}")

    # Chromosome summaries
    for pop, results in results_by_pop.items():
        if len(results) >= 5:
            print(f"Generating chromosome summary for {pop}...")
            try:
                import matplotlib.pyplot as plt
                fig = plot_chromosome_ibd_summary(
                    results,
                    results[0].chrom,
                    figures_dir / f'{pop}_chr2_full_summary.png'
                )
                plt.close(fig)
            except Exception as e:
                print(f"  Warning: {e}")

    # Generate report
    print("\nGenerating report...")
    report = generate_report(results_by_pop, args.output_dir)

    # Print summary
    print("\n" + "=" * 70)
    print("ANALYSIS COMPLETE")
    print("=" * 70)

    for pop in args.populations:
        if pop in results_by_pop:
            stats = compute_summary_statistics(results_by_pop[pop])
            print(f"\n{pop}:")
            print(f"  Pairs: {stats['n_pairs']}")
            print(f"  Total segments: {stats['n_segments']['total']}")
            print(f"  Mean IBD: {stats['total_ibd_mb']['mean']:.2f} Mb")
            if 'segment_length_kb' in stats:
                print(f"  Mean segment: {stats['segment_length_kb']['mean']:.1f} kb")


if __name__ == '__main__':
    main()
