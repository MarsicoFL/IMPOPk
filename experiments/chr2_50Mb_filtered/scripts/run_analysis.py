def analyze_pair(
    pair_key: Tuple,
    data: Dict,
    population: Population,
    expected_ibd_length: float = 100.0,  # Longer for full chr
    min_segment_windows: int = 20,
) -> Optional[IBDResult]:
    """Run IBD inference on a single pair."""
    if len(pair_key) == 5:
        sample_a, hap_a, sample_b, hap_b, chrom = pair_key
    else:
        sample_a, hap_a, sample_b, hap_b = pair_key
        chrom = "CHM13#0#chr2"

    try:
        result = infer_ibd(
            identities=data['identities'],
            window_starts=data['starts'],
            window_ends=data['ends'],
            sample_a=sample_a,
            hap_a=hap_a,
            sample_b=sample_b,
            hap_b=hap_b,
            chrom=chrom,
            population=population,
            expected_ibd_length=expected_ibd_length,
            min_segment_windows=min_segment_windows,
        )
        return result
    except Exception as e:
        print(f"  Error: {sample_a}-{sample_b}: {e}")
        return None


def analyze_population(
    pop_name: str,
    max_pairs: int = 30,
    min_ibd_windows: int = 100,
    expected_ibd_length: float = 100.0,
    min_segment_windows: int = 20,
) -> List[IBDResult]:
    """Analyze IBD for a population on full chr2."""

    print(f"\n{'='*60}")
    print(f"Analyzing {pop_name} - Full Chromosome 2")
    print(f"{'='*60}")

    # Find data file (try multiple naming patterns)
    data_file = None
    for pattern in [f"{pop_name}_chr2_50Mb_ibs.tsv", f"{pop_name}_chr2_full_ibs.tsv"]:
        candidate = DATA_DIR / pattern
        if candidate.exists():
            data_file = candidate
            break

    if data_file is None:
        print(f"  Data file not found for {pop_name}")
        return []

    data = load_full_chr2_ibs(data_file, max_pairs, min_ibd_windows)
    if not data:
        return []

    population = POPULATION_MAP.get(pop_name, Population.EUR)

    results = []
    for i, (pair_key, pair_data) in enumerate(data.items()):
        if i % 5 == 0:
            print(f"  Processing pair {i+1}/{len(data)}")

        result = analyze_pair(
            pair_key, pair_data, population,
            expected_ibd_length, min_segment_windows
        )

        if result:
            results.append(result)

    print(f"  Completed: {len(results)} pairs")

    return results


