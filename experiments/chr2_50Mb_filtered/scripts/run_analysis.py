def compute_summary_statistics(results: List[IBDResult]) -> Dict:
    """Compute summary statistics."""
    if not results:
        return {}

    total_ibd = [r.total_ibd_bp for r in results]
    fractions = [r.fraction_ibd for r in results]
    n_segments = [r.n_segments for r in results]

    all_lengths = []
    all_posteriors = []
    for r in results:
        for s in r.segments:
            all_lengths.append(s.length_bp)
            all_posteriors.append(s.mean_posterior)

    stats = {
        'n_pairs': len(results),
        'total_ibd_mb': {
            'mean': float(np.mean(total_ibd)) / 1e6,
            'median': float(np.median(total_ibd)) / 1e6,
            'std': float(np.std(total_ibd)) / 1e6,
            'min': float(np.min(total_ibd)) / 1e6,
            'max': float(np.max(total_ibd)) / 1e6,
        },
        'fraction_ibd': {
            'mean': float(np.mean(fractions)),
            'median': float(np.median(fractions)),
            'std': float(np.std(fractions)),
        },
        'n_segments': {
            'mean': float(np.mean(n_segments)),
            'median': float(np.median(n_segments)),
            'total': int(np.sum(n_segments)),
        },
    }

    if all_lengths:
        stats['segment_length_kb'] = {
            'mean': float(np.mean(all_lengths)) / 1000,
            'median': float(np.median(all_lengths)) / 1000,
            'std': float(np.std(all_lengths)) / 1000,
            'min': float(np.min(all_lengths)) / 1000,
            'max': float(np.max(all_lengths)) / 1000,
        }
        stats['mean_posterior'] = {
            'mean': float(np.mean(all_posteriors)),
            'min': float(np.min(all_posteriors)),
        }

    return stats


def generate_report(
    results_by_pop: Dict[str, List[IBDResult]],
    output_dir: Path,
) -> str:
    """Generate scientific report for full chr2 analysis."""

    report = []
    report.append("# Full Chromosome 2 IBD Analysis Report")
    report.append("")
    report.append(f"**Generated**: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}")
    report.append(f"**Data Source**: HPRC v2 Pangenome")
    report.append(f"**Chromosome**: chr2 (243.2 Mb)")
    report.append(f"**Window Size**: 5 kb")
    report.append(f"**Method**: HMM with Forward-Backward Algorithm")
    report.append("")

    # Methods
    report.append("## 1. Methods")
    report.append("")
    report.append("### 1.1 Data Generation")
    report.append("")
    report.append("IBS data was generated using `ibs-cli` with the following parameters:")
    report.append("- Reference: CHM13 (T2T)")
    report.append("- Region: chr2:1-243,199,373 (full chromosome)")
    report.append("- Window size: 5,000 bp")
    report.append("- Identity cutoff: 0.999")
    report.append("")

    report.append("### 1.2 IBD Detection Model")
    report.append("")
    report.append("| Population | Non-IBD Mean | Non-IBD Std | IBD Mean | IBD Std |")
    report.append("|------------|--------------|-------------|----------|---------|")

    for pop in ['AFR', 'EUR', 'EAS']:
        if pop in POPULATION_MAP:
            params = HMMParams.from_population(POPULATION_MAP[pop])
            report.append(f"| {pop} | {params.emission_non_ibd.mean:.5f} | "
                         f"{params.emission_non_ibd.std:.5f} | "
                         f"{params.emission_ibd.mean:.4f} | {params.emission_ibd.std:.4f} |")

    report.append("")

    # Results
    report.append("## 2. Results")
    report.append("")
    report.append("### 2.1 Summary Statistics")
    report.append("")
    report.append("| Population | Pairs | Mean IBD (Mb) | Mean Fraction | Segments | Mean Length (kb) |")
    report.append("|------------|-------|---------------|---------------|----------|------------------|")

    for pop in ['AFR', 'EUR', 'EAS']:
        if pop in results_by_pop and results_by_pop[pop]:
            stats = compute_summary_statistics(results_by_pop[pop])
            report.append(f"| {pop} | {stats['n_pairs']} | "
                         f"{stats['total_ibd_mb']['mean']:.2f} | "
                         f"{stats['fraction_ibd']['mean']:.3f} | "
                         f"{stats['n_segments']['total']} | "
                         f"{stats.get('segment_length_kb', {}).get('mean', 0):.1f} |")

    report.append("")

    # Longest segments
    report.append("### 2.2 Longest IBD Segments")
    report.append("")

    for pop in ['AFR', 'EUR', 'EAS']:
        if pop in results_by_pop and results_by_pop[pop]:
            all_segments = []
            for r in results_by_pop[pop]:
                for s in r.segments:
                    all_segments.append((r.sample_a, r.sample_b, s))

            all_segments.sort(key=lambda x: -x[2].length_bp)

            if all_segments:
                report.append(f"**{pop}**:")
                for sample_a, sample_b, seg in all_segments[:5]:
                    report.append(f"- {sample_a} - {sample_b}: {seg.length_bp/1e6:.2f} Mb "
                                 f"(posterior: {seg.mean_posterior:.3f}, "
                                 f"pos: {seg.start_bp/1e6:.1f}-{seg.end_bp/1e6:.1f} Mb)")
                report.append("")

    # Quality metrics
    all_posteriors = []
    for results in results_by_pop.values():
        for r in results:
            for s in r.segments:
                all_posteriors.append(s.mean_posterior)

    if all_posteriors:
        report.append("### 2.3 Quality Metrics")
        report.append("")
        report.append(f"- Total segments detected: {len(all_posteriors)}")
        report.append(f"- Mean posterior probability: {np.mean(all_posteriors):.3f}")
        report.append(f"- Segments with P(IBD) > 0.9: {np.sum(np.array(all_posteriors) > 0.9) / len(all_posteriors) * 100:.1f}%")
        report.append(f"- Segments with P(IBD) > 0.95: {np.sum(np.array(all_posteriors) > 0.95) / len(all_posteriors) * 100:.1f}%")
        report.append("")

    # Conclusions
    report.append("## 3. Key Findings")
    report.append("")

    pop_by_length = [(p, compute_summary_statistics(r).get('segment_length_kb', {}).get('mean', 0))
                    for p, r in results_by_pop.items() if r]
    pop_by_length.sort(key=lambda x: -x[1])

    if pop_by_length:
        report.append(f"1. **Longest mean IBD segments**: {pop_by_length[0][0]} ({pop_by_length[0][1]:.1f} kb)")
        report.append("   - Consistent with population bottleneck history")
        report.append("")

    report.append("## 4. Conclusion")
    report.append("")
    report.append("This full chromosome analysis provides comprehensive IBD patterns across")
    report.append("the entire chr2, enabling detection of both short and long IBD segments")
    report.append("with rigorous posterior probability assessment.")
    report.append("")

    report_text = '\n'.join(report)

    report_path = output_dir / "FULL_CHR2_IBD_REPORT.md"
    with open(report_path, 'w') as f:
        f.write(report_text)

    print(f"\nReport: {report_path}")

    return report_text


