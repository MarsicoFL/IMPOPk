def parse_haplotype_name(name: str) -> Tuple[str, int]:
    """Extract sample ID and haplotype number from HPRC format."""
    parts = name.split('#')
    if len(parts) >= 2:
        try:
            return parts[0], int(parts[1])
        except ValueError:
            return parts[0], 0
    return name, 0


def load_full_chr2_ibs(filepath: Path, max_pairs: int = None, min_ibd_windows: int = 50) -> Dict:
    """
    Load full chromosome 2 IBS data.

    This function handles the full chr2 data (~48K windows) efficiently.
    It reconstructs complete tracks by:
    1. Collecting all unique windows
    2. For each pair, tracking which windows have high IBS (potential IBD)
    3. Building complete chromosome tracks

    Args:
        filepath: Path to IBS TSV file
        max_pairs: Maximum pairs to return (by IBD window count)
        min_ibd_windows: Minimum IBD windows to include a pair

    Returns:
        Dictionary with pair data for IBD inference
    """
    print(f"Loading: {filepath.name}")

    # First pass: collect all windows and pair data
    all_windows = set()  # (start, end)
    pair_ibd_windows = defaultdict(set)  # pair_key -> set of (start, end)
    pair_identities = defaultdict(dict)  # pair_key -> {(start, end): identity}
    chrom = None

    line_count = 0
    with open(filepath, 'r') as f:
        header = f.readline().strip().split('\t')

        for line in f:
            line_count += 1
            if line_count % 1000000 == 0:
                print(f"  Processed {line_count:,} lines...")

            parts = line.strip().split('\t')
            if len(parts) < 6:
                continue

            chrom_full, start, end, group_a, group_b, identity = parts[:6]

            try:
                start = int(start)
                end = int(end)
                identity = float(identity)
            except ValueError:
                continue

            if chrom is None:
                chrom = chrom_full

            sample_a, hap_a = parse_haplotype_name(group_a)
            sample_b, hap_b = parse_haplotype_name(group_b)

            # Canonical key
            if sample_a > sample_b or (sample_a == sample_b and hap_a > hap_b):
                sample_a, hap_a, sample_b, hap_b = sample_b, hap_b, sample_a, hap_a

            window_key = (start, end)
            pair_key = (sample_a, hap_a, sample_b, hap_b)

            all_windows.add(window_key)
            pair_ibd_windows[pair_key].add(window_key)
            pair_identities[pair_key][window_key] = identity

    print(f"  Total lines: {line_count:,}")
    print(f"  Total unique windows: {len(all_windows):,}")
    print(f"  Total haplotype pairs: {len(pair_ibd_windows):,}")

    # Sort windows by position
    sorted_windows = sorted(all_windows)

    # Build complete tracks
    result = {}
    filtered_count = 0

    for pair_key, ibd_windows in pair_ibd_windows.items():
        n_ibd = len(ibd_windows)

        if n_ibd < min_ibd_windows:
            filtered_count += 1
            continue

        sample_a, hap_a, sample_b, hap_b = pair_key

        # Build complete track
        starts = []
        ends = []
        identities = []

        for start, end in sorted_windows:
            starts.append(start)
            ends.append(end)

            if (start, end) in ibd_windows:
                identities.append(pair_identities[pair_key].get((start, end), 1.0))
            else:
                # Non-IBD window - use background identity
                identities.append(0.999)

        result[pair_key + (chrom,)] = {
            'starts': np.array(starts),
            'ends': np.array(ends),
            'identities': np.array(identities),
            'n_total_windows': len(starts),
            'n_ibd_windows': n_ibd,
            'ibd_fraction': n_ibd / len(starts) if starts else 0,
        }

    print(f"  Pairs with >= {min_ibd_windows} IBD windows: {len(result)}")
    print(f"  Pairs filtered: {filtered_count}")

    # Keep top pairs by IBD
    if max_pairs and len(result) > max_pairs:
        sorted_pairs = sorted(result.items(), key=lambda x: -x[1]['n_ibd_windows'])
        result = dict(sorted_pairs[:max_pairs])
        print(f"  Keeping top {max_pairs} pairs")

    return result


